# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

list_1 = [1,2,3,4,7,6,5,8,23] #объявили начальный список
list_2 = [] #задали пустой список в который будем записывать данные
filter = lambda x: x/4 if (x%2)==0 else x*2 #используем функцию фильтр при условии - если заданное число строки
#делится на 2 нацело (остаток от деления равен 0), тогда это число делится на 4, иначе умножается на 2
for d in list_1:
    list_2.append(filter(d))

print('Исходный список: ', list_1, '\n', 'Результат преобразований: ', list_2)
