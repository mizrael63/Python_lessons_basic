# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()
номер=1
фрукты = ["яблоко", "апельсин", "кокос", "манго", "ананас", "лимон"] #создали список фруктов
for фрукт in фрукты: #для элемента списка выполнять
    print('{}.{:>15}'.format(номер, фрукт)) #печатать номер, который за счёт цикла увеличивается и
#после него значение фрукта прошедшее функцию формат
    номер += 1


